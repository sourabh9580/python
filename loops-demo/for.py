# simple range
print("simple range")
for i in range(10):
    print(i, end=' ')

print()

# range with start and stop
print("range with start and stop")
for i in range(5, 10):
    print(i, end=' ')

print()

# range with start, stop and step
print("range with start, stop and step")
for i in range(5, 10, 2):
    print(i, end=' ')

print()

# simple tuple
print("simple tuple")
for i in (5, 10, 15):
    print(i, end=' ')

print()

# expression in range
print("expression in range")
n = 5
for i in range(2*n + 3):
    print(i, end=' ')

print()
